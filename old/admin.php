<h1>Administration des Kalenders</h1>
	Die Kalender werden weiterhin über die Officekalender gepflegt. Die Kalender sind hier im Onlinesystem hinterlegt.<br>
	Ein Kalender kann Zusatzkalender sein oder nicht. Ein Zusatzkalender kann keine unterkategorien haben.
	<h2>Tags</h2>
	Jeder Kalender (außer Zusatzkalender) kann mithilfe von Tags unterteilt werden. Diese Tags werden von den Kalenderverwaltern in das Beschreibungsfeld geschrieben.<br>
	Es können auch mehrere tags verwendet werden. Zu jedem Kalender gibt es passende Tags. Tags aus Kalendern können nicht bei anderen Kalendern verwendet werden.
	In Zusatzkalendern können keine Tags verwendet werden.
	<h2>Aktualisierung der Daten</h2>
	Die Kalender im Onlinesystem aktualisieren sich alle 5 Minuten.
